// Declare a variable 'name' and assign the value "Harry" to it.
let name = "Harry"

// Log the length of the 'name' string.
console.log(name.length)

// Convert 'name' to uppercase and log the result.
console.log(name.toUpperCase())

// Convert 'name' to lowercase and log the result.
console.log(name.toLowerCase())

// Extract a substring of 'name' from index 2 to 4 (exclusive) and log it.
console.log(name.slice(2, 4))

// Extract a substring of 'name' starting from index 2 to the end and log it.
console.log(name.slice(2))

// Replace the substring "Har" with "Per" in 'name' and log the result.
console.log(name.replace("Har", "Per"))

// Declare a variable 'friend' and assign the value "Naman" to it.
let friend = "Naman"

// Concatenate strings to create a sentence and log it.
console.log(name.concat(" is a friend of ", friend, " Ok"))

// Declare a variable 'friend2' with extra spaces at the beginning and end.
let friend2 = "      Meena        "

// Log the 'friend2' string as it is.
console.log(friend2)

// Trim extra spaces from 'friend2' and log the result.
console.log(friend2.trim())

